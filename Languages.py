def getalphabetfromhex(beginninghex, endhex): # hex is the hexadecimal writing
    beginning = int(beginninghex, 16)
    end = int(endhex, 16)
    end = end + 1

    Alphabet = []
    for i in range(beginning, end):
        Alphabet.append(chr(i))
    return Alphabet


